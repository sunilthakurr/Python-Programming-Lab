# changing and adding dictionary elements

dictionary = {'Name' : 'Sunil Kumar','Section':'F'}

dictionary['Section'] = 'Updated soon'                                # it will update the value of section
print(dictionary)                                                     # {'Name': 'Sunil Kumar', 'Section': 'Updated soon'}
dictionary['Roll Number'] = 49                                        # it will add roll number to dictionary
print(dictionary)                                                     # {'Name': 'Sunil Kumar', 'Section': 'Updated soon', 'Roll Number': 49}